#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	char	*new_str;
	size_t	len;

	i = 0;
	len = (ft_strlen(s1) + ft_strlen(s2) + 1);
	new_str = (char *)malloc(len * sizeof(char));
	if (!new_str)
		return (NULL);
	j = 0;
	while (s1[j])
		new_str[i++] = s1[j++];
	j = 0;
	while (s2[j])
		new_str[i++] = s2[j++];
	new_str[i] = '\0';
	return (new_str);
}
